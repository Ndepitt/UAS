    private void bukutamuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bukutamuMouseClicked
        // TODO add your handling code here:
        
        int selectedRow = bukutamu.getSelectedRow();
        
        TableModel model = bukutamu.getModel();
        innama.setText(model.getValueAt(selectedRow, 0).toString());
        inalamat.setText(model.getValueAt(selectedRow, 1).toString());
        innohp.setText(model.getValueAt(selectedRow, 2).toString());
        String jeniskelamin = model.getValueAt(selectedRow, 3).toString();
            if(jeniskelamin.equals("l")){
                inlakilaki.setSelected(true);
            } else if (jeniskelamin.equals("p")){
                inperempuan.setSelected(true);
            }
    }//GEN-LAST:event_bukutamuMouseClicked

