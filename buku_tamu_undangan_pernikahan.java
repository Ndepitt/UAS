    public void table() {

        Koneksi konek = new Koneksi();
        konek.Koneksi();

        try {

            String sql = "select * from bukutamu";
            pst = konek.con.prepareStatement(sql);
            rs = pst.executeQuery(sql);
            bukutamu.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }
